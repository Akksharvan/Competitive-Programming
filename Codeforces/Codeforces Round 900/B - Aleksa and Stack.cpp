#include <bits/stdc++.h>
using namespace std;
    
void solve() {
    int N; cin >> N;
    for (int i = 0; i < N; i++) cout << (i + 10) << " ";

    cout << "\n";
    return;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int T; cin >> T;
    while (T--) solve();
    
    return 0;
}